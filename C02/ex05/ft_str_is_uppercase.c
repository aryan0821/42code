int	ft_str_is_uppercase(char *str)
{
	int	i;
	int	res;

	i = 0;
	res = 0;
	while (str[i] != '\0')
	{
		if (!(str[i] >= 'A' && str[i] <= 'Z'))
			return (res);
		i++;
	}
	res = 1;
	return (res);
}
