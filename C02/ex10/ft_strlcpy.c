unsigned int	ft_strlcpy(char *dest, char *src, unsigned int size)
{
	unsigned int	i;
	unsigned int	src_sz;

	i = 0;
	src_sz = 0;
	while (src[src_sz] != '\0')
		src_sz++;
	if (size < 1)
		return (src_sz);
	while (src[i] && i < size - 1)
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (src_sz);
}
