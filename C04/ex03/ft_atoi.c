int	ft_atoi(char *str)
{
	int	i;
	int	ret;
	int	n;

	n = 0;
	i = 0;
	ret = 0;
	while (str[i] && ((str[i] >= 9 && str[i] <= 13) || str[i] == 32))
		i++;
	while (str[i] && (str[i] == '+' || str[i] == '-'))
	{
		if (str[i] == '-')
			n++;
		i++;
	}
	while (str[i] && ((str[i] >= '0') && (str[i] <= '9')))
	{
		ret = (str[i] - '0') + (ret * 10);
		i++;
	}
	if (n % 2 == 1)
		return (ret * -1);
	return (ret);
}
